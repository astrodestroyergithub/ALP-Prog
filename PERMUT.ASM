********** PERMUTATION **********
;******************************
; ALP TO FIND NPR
; MAX. N=8 (AS 9! > 16-BIT)
;*****************************
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ORG 1000H
N EQU 08H
R EQU 05H
TEMP DW ?
NPR DW ?
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
XOR DX,DX
MOV BX,N-R
CMP BX,01H
JNZ LP1
MOV TEMP,01H
JMP LP2
LP1:CALL FACT
MOV TEMP,AX
XOR AX,AX
LP2:MOV BX,N
CALL FACT
MOV BX,TEMP
DIV BX ; (DX|AX)/BX, QUO-AX
MOV NPR,AX
HLT
FACT PROC
MOV CX,00H
MOV CX,BX
DEC CX
MOV AX, BX
PROC_LP: DEC BX
MUL BX
LOOP PROC_LP
RET
FACT ENDP
CODE ENDS
END START