********** 8087 ALP TO FIND THE AREA OF A SQUARE **********
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
.8087
ORG 0000H
START:
MOV AX, DATA
MOV DS, AX
FINIT
FLD SIDE ; ST(0)-->SIDE
FST ST(4) ; ST(4)-->SIDE
FMUL ST(0), ST(4) ; ST(0)--> S^2
FST AREA
HLT
CODE ENDS
DATA SEGMENT
SIDE DD 2.534
AREA DD ?
DATA ENDS
END START