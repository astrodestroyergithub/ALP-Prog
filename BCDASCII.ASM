********** MUTUAL CONVERSION OF BCD AND ASCII NUMBERS **********
CODE SEGMENT
ASSUME CS:CODE
START:
MOV AL,56H ;BCD TO ASCII CONVERSION
MOV AH,AL
AND AL,0FH
MOV CL,4H
SHR AH,CL
AND AH,0FH
OR AX,3030H
AND AX,0F0FH ;CONVERTING THE 2 ASCII VALUES TO BCD
XOR BX,BX
MOV BX,AX
SHR BX,CL
OR AX,BX
MOV AH, 2 ;DISPLAYING THE CHARACTER EQUIVALENT OF THE BCD NUMBER
MOV DL,AL
INT 21H
HLT
CODE ENDS
END START