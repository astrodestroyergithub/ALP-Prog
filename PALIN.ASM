********** ALP TO CHECK FOR A PALINDROME **********
DATA SEGMENT
ORG 1000H
STR1 DB 9H DUP(0)
MSG1 DB 0AH,0DH,"ENTERED STRING IS A PALINDROME",0AH,0DH,"$"
MSG2 DB 0AH,0DH,"ENTERED STRING IS NOT A PALINDROME",0AH,0DH,"$"
LEN EQU 09H
DATA ENDS
EXTRA SEGMENT
ORG 2000H
STR2 DB 9H DUP(0)
EXTRA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA,ES:EXTRA
START: MOV AX,DATA
MOV DS,AX
MOV AX,EXTRA
MOV ES,AX
MOV SI,OFFSET STR1
MOV CX,LEN
L1: MOV AH,01H
INT 21H
MOV [SI],AL
INC SI
LOOP L1
LEA SI,STR1
LEA DI,STR2+LEN-1
MOV CX,LEN
L2: CLD
LODSB; Loads AX by content of string pointed by DS:SI pair
STD
STOSB; Stores AX content to a location pointed by ES:DI pair
LOOP L2
MOV SI,OFFSET STR1
MOV DI,OFFSET STR2
MOV CX,LEN
CLD
REPE CMPSB
JZ YES
JMP NO
YES: LEA DX,OFFSET MSG1
MOV AH,09H
INT 21H
JMP STP
NO: LEA DX,OFFSET MSG2
MOV AH,09H
INT 21H
STP: HLT
CODE ENDS
END START
