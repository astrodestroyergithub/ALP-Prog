********** SUM AND AVERAGE OF 8-BIT ARRAY NUMBERS **********
ASSUME CS:CODE, DS:DATA
DATA SEGMENT
MYARRAY DW 43H,60H,54H,49H,79H,90H,56H,97H,82H,79H
SUMRES DW ?
DIVQUO1 DW ?
DIVREM1 DW ?
DATA ENDS
CODE SEGMENT
START:
MOV CX,000AH
MOV BX,CX
MOV AX,DATA
MOV DS,AX
XOR AX,AX
LEA SI,MYARRAY
RPT:
ADD AX,MYARRAY[SI]
ADD SI,02H
LOOP RPT
MOV SUMRES,AX
XOR DX,DX
MOV AX,AX
DIV BX
MOV DIVQUO1,AX
MOV DIVREM1,DX
HLT
CODE ENDS
END START