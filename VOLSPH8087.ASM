********** 8087 ALP FOR FINDING VOLUME OF SPHERE **********
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
.8087
ORG 000AH
START:
MOV AX, DATA
MOV DS, AX
FINIT
FLD RADIUS ; ST(0)-->RADIUS
FST ST(4) ; ST(4)-->RADIUS
FMUL ST(0), ST(4) ; ST(0)--> R^2
FMUL ST(0), ST(4) ; ST(0)-->R^3
FLD CONST ; ST(0)-->CONST(4/3, ST(1)-->R^3
FMUL ; ST(0)-->(4/3)*(R^3)
FLDPI ; ST(0)-->PI, ST(1)-->(4/3)*(R^3)
FMUL ; ST(0)-->PI*(4/3)*(R^3)
FST VOLUME
HLT
CODE ENDS
DATA SEGMENT
RADIUS DD 7.342
CONST DD 1.333
VOLUME DD ?
DATA ENDS
END START