********** COMBINATION **********
;******************************
; ALP TO FIND NCR
; MAX. N=8 (AS 9! > 16-BIT)
;*****************************
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
ORG 1000H
N EQU 08H
R EQU 05H
TEMP1 DW ?
TEMP2 DW ?
NCR DW ?
DATA ENDS
CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
XOR DX,DX
MOV BX,N-R
CMP BX,01H
JNZ LP1
MOV TEMP1,01H
LP1:CALL FACT
MOV TEMP1,AX
XOR AX,AX
MOV BX,R
CMP BX,01H
JNZ LP3
MOV TEMP2,01H
LP3:CALL FACT
MOV TEMP2,AX
XOR AX,AX
JMP LP2
LP2:MOV BX,N
CALL FACT
MOV BX,TEMP1
DIV BX ; (DX|AX)/BX, QUO-AX
XOR DX,DX
MOV BX,TEMP2
DIV BX ; (DX|AX)/BX, QUO-AX
MOV NCR,AX
HLT
FACT PROC
MOV CX,00H
MOV CX,BX
DEC CX
MOV AX, BX
PROC_LP: DEC BX
MUL BX
LOOP PROC_LP
RET
FACT ENDP
CODE ENDS
END START