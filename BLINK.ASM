********** DISPLAY BLINKING CHARACTER **********
DATA SEGMENT
CHA1 DB 50 DUP(' '),'$'
MSG1 DB 'ENTER TEXT TILL T: $'
COL DB 1EH
DATA ENDS
DISP MACRO MSG
MOV AH, 09H
LEA DX, MSG
INT 21H
ENDM
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
LEA SI,CHA1
DISP MSG1
READ:
MOV AH, 01H ; GETS A CHAR FROM KEYBOARD, STORES ASCII IN AH
INT 21H
CMP AL, 54H ; CHECKS FOR 'T'
JE MIDDLE
MOV [SI], AL
INC SI
JMP READ
MIDDLE:
MOV DL,0AH ; SETTING COLUMN VALUE FOR CURSOR
MOV AH,02H
INT 21H
MOV DL,09H
MOV AH,02H
INT 21H
MOV DL,09H
MOV AH,02H
INT 21H
MOV DL,09H
MOV AH,02H
INT 21H
MOV DL,09H
MOV AH,02H
INT 21H
COLOUR:
MOV AH,09H
MOV AL,54H ; MOVES 'T' IN AL
MOV CX,01H ; NUMBER OF TIMES OF OCCURUNCES OF 'T'
MOV BL,COL ; SETTING COLOUR ATTRIBUTE
BLINK: OR BL,10000000b
MOV BH,0H
INT 10H
HLT
CODE ENDS
END START