********** MULTIPLY 2 MATRICES AND DISPLAY THE RESULT **********
DATA SEGMENT
AR1 DB 01H,01H,01H ;ROW1 OF ARRAY A
AR2 DB 02H,02H,02H ;ROW2 OF ARRAY A
AR3 DB 03H,03H,03H ;ROW3 OF ARRAY A
BC1 DB 04H,05H,06H ;COLUMN1 OF ARRAY B
BC2 DB 04H,05H,06H ;COLUMN2 OF ARRAY B
BC3 DB 04H,05H,06H ;COLUMN3 OF ARRAY B
RES DB 9 DUP(?) ;RESULT MATRIX
L2 DB ?
L1 DB ?
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV BP,00H
MOV L2,03H
LEA SI,AR1
REP2:
LEA DI,BC1
MOV L1,03H
REP1:
CALL MATMUL
MOV DS:RES[BP],DL
INC BP
ADD DI,03H
DEC L1
JNZ REP1
ADD SI,03H
DEC L2
JNZ REP2
INT 3
MATMUL PROC
MOV CX,03H
MOV BX,00H
MOV DL,00H
AGAIN:
MOV AL,[SI][BX]
IMUL BYTE PTR [DI][BX]
ADD DL,AL
INC BX
LOOP AGAIN
RET
MATMUL ENDP
ALIGN 16
HLT
CODE ENDS
END START
