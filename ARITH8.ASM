********** 16-BIT ARITHMETIC OPERATION **********
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX,DATA
MOV DS,AX
XOR AX,AX
MOV AX,2001H ;INPUT HIGHER NUMBER
MOV BX,1403H ;INPUT LOWER NUMBER
ADD AX,BX
MOV SUMRES1,AX
MOV AX,2001H
SUB AX,BX
MOV DIFFRES1,AX
MOV AX,2001H
MUL BX ;16-BIT MULTIPLICAND IN AX & MULTIPLIER IN ANY GP REGISTER
MOV MULRES1,AX
MOV MULRES2,DX
XOR DX,DX
MOV AX,2001H
DIV BX
MOV DIVQUO1,AX;QUOTIENT OF 16-BIT DIVISION IS STORED IN AX
MOV DIVREM1,DX;REMAINDER OF 16-BIT DIVISION IS STORED IN DX
HLT
CODE ENDS
DATA SEGMENT
ORG 1200H ;DEFINES THE BEGINNING OFFSET FOR DATA SEGMENT
SUMRES1 DW ? ;USED TO STORE THE RESULT OF ADDITION
DIFFRES1 DW ?
MULRES1 DW ?
MULRES2 DW ?
DIVQUO1 DW ?
DIVREM1 DW ?
DATA ENDS
END
END